package uk.co.loomknitting.marketstalltrader;

public class DatabaseModel {
    private String amountPaid;

    public String getAmountPaid() {
        return amountPaid;
    }

    public void setAmountPaid(String amountPaid) {
        this.amountPaid = amountPaid;
    }

    public String getDateTimePaid() {
        return dateTimePaid;
    }

    public void setDateTimePaid(String dateTimePaid) {
        this.dateTimePaid = dateTimePaid;
    }

    public int getPaymentMethod() {
        return paymentMethod;
    }

    public void setPaymentMethod(int paymentMethod) {
        this.paymentMethod = paymentMethod;
    }

    public String getObject() {
        return object;
    }

    public void setObject(String object) {
        this.object = object;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public int getIdSales() {
        return idSales;
    }

    public void setIdSales(int idSales) {
        this.idSales = idSales;
    }

    public int getIdInventory() {
        return idInventory;
    }

    public void setIdInventory(int idInventory) {
        this.idInventory = idInventory;
    }

    private String dateTimePaid;
    private int paymentMethod;
    private int idSales;
    private int idInventory;
    private String object;
    private String description;
    private String cost;
}
